sQuote Privacy Policy
 
 

The data you store in your sQuote account belongs to you, you own it, you always have access to it, and you can request us to destroy it at any time.

 

sQuote does not use the personal data you enter in your customer accounts in our service for any purposes, nor do we sell this data or use it for any marketing purposes.

 

All data is only accessible by our staff and only for the purposes of providing you with the service or improvements thereto.

 

GDPR

To the best of our knowledge our systems, processes and policies comply fully with the GDPR from a saas providers perspective. Full compliance requires that our users also meet these regulations, therefore it is up to each user to ensure full compliance. 

 

 

We collect personal and activity data, which may be linked.

We may use technologies like cookies (small files stored on your browser), web beacons, or unique device identifiers to identify your computer or device. This data is only used within the sQuote environment in an effort to provide to you the services that we offer.


We collect personally identifiable information that you provide to us, such as your name, address, phone number or email address. Our systems and third-party CRM’s may associate this personal information with your activities in the course of providing service to you (such as your activity inside the sQuote Service).


We collect or share your location only with permission.

In serving you, we may use or store your precise geographic location in our Apps as you give us permission to do so. We do not use or share this data for any other purpose. Many devices will indicate through an icon when location services are operating. We only share this location information with others as approved by you.


You can request to see your personal data.

You can sign into your account to see and edit any personally identifiable information we have stored, such as your name, email, address or phone number. You can also contact us by email to request to see this information.

​We don’t share your personal data with other companies for profit.

We do not share personally identifiable information (such as name, address, email or phone) with other companies in exchange for money or other forms of income.

​

You can ask privacy questions.

If you have any questions or concerns about our privacy policies, please email privacy@sQuote.net

​

Analytics providers access data on our behalf.​

Analytics companies may access “anonymous” data (such as your IP address or device ID) to help us understand how our services are used. They use this data solely on our behalf. They do not share it except in aggregate form; no data is shared as to any individual user.

​

We take detailed steps to protect personal information.

We take reasonable administrative, physical and electronic measures designed to safeguard and protect your information from unauthorized access or disclosure. This includes utilizing Secure Sockets Layer (SSL) software and storing your information behind a firewall designed to block access from outside our network. However, no security method can be guaranteed to protect information from hackers or human error. Information we collect may be stored or processed on computers located in any country where we do business. sQuote will not sell or otherwise provide your personal information to a third party or make any other use of personal information about you for any purpose which is not incidental to the normal use of the Service.

 

Current Storage Processes and Protection

As at writing, sQuote utilises the servers of Amazon Web Services (AWS) and others to host and protect your data. AWS, at time of publishing, advise that all their service providing’s are GDPR compliant. sQuote utilises Northern and Southern Hemisphere AWS servers with data mirroring so that should a disaster occur in one location our service can continue. In addition, sQuote copies all data on a daily basis and stores it in a 3rd country on a non-AWS server. We believe we adhere to industry best practice for the protection of our user’s data and for the continuity of service.

​

Special situations may require disclosure of your data.

To operate the service, we also may make identifiable and anonymous information available to third parties in these limited circumstances:

         

1. with your express consent,

2. when we have a good faith belief it is required by law,

3. when we have a good faith belief it is necessary to protect our rights or property, or

4. to any successor or purchaser in a merger, acquisition, liquidation, dissolution or sale of assets. Your consent will not be required for disclosure in these cases, but we will attempt to notify you, to the extent permitted by law to do so.

 

 

